    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String nombre = txtNombre.getText();
        String apellido = txtapellido.getText();
        String email = txtemail.getText();
        String clave = txtpassword.getText();
        String tipousuario = combotipo.getSelectedItem().toString();
        
        // Validar campos
        if(nombre.isEmpty() || apellido.isEmpty() || email.isEmpty() || clave.isEmpty()) {
            JOptionPane.showMessageDialog(null, "DEBE COMPLETAR LOS CAMPOS VAC√çOS");
        } else {
            if(tipousuario.equalsIgnoreCase("Seleccionar")) {
                JOptionPane.showMessageDialog(null, "DEBE SELECCIONAR UN TIPO DE USUARIO");
            } else {
                // Crear un nuevo usuario sin id (autoincremental)
                Usuario usuario = new Usuario(0, nombre, apellido, email, clave, tipousuario);
                
                // Crear una instancia de UsuarioDAO
                UsuarioDAO uDao = new UsuarioDAO();
                
                // Registrar el usuario
                uDao.RegistrarUsuario(usuario);
                
                // Navegar a la pantalla de login
                Login login = new Login();
                login.setVisible(true);
                this.dispose();
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed
    
    
